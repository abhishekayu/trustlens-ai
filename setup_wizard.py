def _setup_cloud_provider(provider_key: str, provider_name: str, color: str) -> dict[str, str] | None:
    """Interactive cloud provider setup: get API key, optional model override."""
    default_model = DEFAULT_MODELS[provider_key]

    print(f"\n{color}{BOLD}{provider_name} Setup{RESET}")
    print(f"{DIM}{'─' * 50}{RESET}")

    # API Key URLs
    urls = {
        "grok": "https://console.x.ai",
        "gemini": "https://aistudio.google.com/apikey",
        "anthropic": "https://console.anthropic.com/settings/keys",
        "openai": "https://platform.openai.com/api-keys",
    }

    url = urls.get(provider_key, "")
    if url:
        print(f"\n   {DIM}Get your API key at: {CYAN}{url}{RESET}")

    while True:
        try:
            api_key = input(f"\n{BOLD}➤ Paste your API key: {RESET}").strip()
        except (EOFError, KeyboardInterrupt):
            return None

        if not api_key:
            print(f"{RED}   API key cannot be empty.{RESET}")
            continue

        if len(api_key) < 10:
            print(f"{RED}   That doesn't look like a valid API key. Try again.{RESET}")
            continue

        break

    # Model selection
    print(f"\n   {DIM}Default model: {WHITE}{BOLD}{default_model}{RESET}")
    try:
        custom = input(f"{BOLD}➤ Model name (Enter for default): {RESET}").strip()
    except (EOFError, KeyboardInterrupt):
        custom = ""

    model = custom if custom else default_model

    key_prefix = provider_key.upper()
    result = {
        "TRUSTLENS_AI_PROVIDER": provider_key,
        f"TRUSTLENS_{key_prefix}_API_KEY": api_key,
        f"TRUSTLENS_{key_prefix}_MODEL": model,
    }

    print(f"\n{GREEN}✅ {provider_name} configured!{RESET}")
    print(f"   {DIM}Provider: {provider_key} | Model: {model}{RESET}")

    return result


# ── Main Wizard ───────────────────────────────────────────────────────


def run_wizard() -> dict[str, str] | None:
    """
    Run the interactive LLM setup wizard.

    Returns the env config dict on success, None on cancel.
    Returns "continue" string if user wants to keep saved config.
    """
    _print_banner()

    # ── Check for saved config ────────────────────────────────────────
    saved = _get_saved_config()

    if saved:
        provider_name = saved["provider"].upper()
        model = saved.get("model", "default")
        has_key = saved.get("has_key", "")

        print(f"{GREEN}{BOLD}  ✅ Saved configuration found:{RESET}")
        print(f"     Provider: {CYAN}{BOLD}{provider_name}{RESET}")
        print(f"     Model:    {WHITE}{BOLD}{model}{RESET}")
        if has_key:
            key_status = f"{GREEN}set{RESET}" if has_key == "yes" else f"{RED}not set{RESET}"
            print(f"     API Key:  {key_status}")

        print(f"\n   {GREEN}{BOLD}C.{RESET} ▶  Continue with saved config")
        print(f"   {YELLOW}{BOLD}N.{RESET} 🔄 Pick a new LLM provider")
        print(f"   {RED}{BOLD}Q.{RESET} ✖  Quit\n")

        while True:
            try:
                choice = input(f"{BOLD}➤ Choice (C/N/Q): {RESET}").strip().lower()
            except (EOFError, KeyboardInterrupt):
                print(f"\n{YELLOW}👋 Exiting.{RESET}")
                return None

            if choice in ("c", "continue", ""):
                print(f"\n{GREEN}▶  Continuing with {CYAN}{BOLD}{provider_name}{RESET}{GREEN} ({model}){RESET}")
                return "continue"  # type: ignore[return-value]
            elif choice in ("n", "new"):
                break  # Fall through to provider selection
            elif choice in ("q", "quit"):
                return None
            else:
                print(f"{RED}   Enter C, N, or Q.{RESET}")

        print()  # blank line before provider list

    # ── Provider selection ────────────────────────────────────────────
    print(f"{WHITE}{BOLD}  Choose your LLM provider:{RESET}\n")

    for p in PROVIDERS:
        print(f"   {p['color']}{BOLD}{p['num']}.{RESET} {p['icon']}  {p['name']}")

    print(f"\n   {DIM}Press Ctrl+C to exit{RESET}")

    while True:
        try:
            choice = input(f"\n{BOLD}➤ Select provider (1-4): {RESET}").strip()
        except (EOFError, KeyboardInterrupt):
            print(f"\n{YELLOW}👋 Setup cancelled.{RESET}")
            return None

        provider = next((p for p in PROVIDERS if p["num"] == choice), None)
        if not provider:
            print(f"{RED}   Invalid choice. Enter 1-4.{RESET}")
            continue

        return _setup_cloud_provider(
            provider["key"], provider["name"], provider["color"]
        )


# ── Entry point ───────────────────────────────────────────────────────


def main() -> None:
    """
    Entry point: run wizard → save config → start uvicorn.
    Config is saved persistently. Use 'N' at next startup to pick a new provider.
    """
    # Run the wizard
    config = run_wizard()
    if config is None:
        print(f"\n{RED}Setup cancelled. Exiting.{RESET}")
        sys.exit(1)

    # If user chose "Continue", config is already in .env — just start
    if config == "continue":
        provider = _read_env().get("TRUSTLENS_AI_PROVIDER", "unknown")
    else:
        # Write new config to .env
        _write_env(config)
        provider = config.get("TRUSTLENS_AI_PROVIDER", "unknown")

    print(f"\n{GREEN}{BOLD}{'═' * 50}{RESET}")
    print(f"{GREEN}{BOLD}  🚀 Starting TrustLens AI with {provider.upper()}{RESET}")
    print(f"{GREEN}{BOLD}{'═' * 50}{RESET}\n")

    # Start uvicorn
    try:
        os.chdir(ENV_FILE.parent)

        subprocess.run(
            [
                sys.executable,
                "-m",
                "uvicorn",
                "trustlens.main:app",
                "--host",
                "0.0.0.0",
                "--port",
                "8000",
                "--reload",
            ],
            env={**os.environ, "TRUSTLENS_WIZARD_DONE": "1"},
            check=True,
        )
    except subprocess.CalledProcessError:
        print(f"\n{RED}Server exited with error.{RESET}")
    except KeyboardInterrupt:
        print(f"\n{YELLOW}👋 Server stopped.{RESET}")


if __name__ == "__main__":
    main()
