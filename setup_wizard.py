# ── .env helpers ──────────────────────────────────────────────────────


def _read_env() -> dict[str, str]:
    """Read .env into an ordered dict (preserves comments as keys)."""
    data: dict[str, str] = {}
    if not ENV_FILE.exists():
        return data
    for line in ENV_FILE.read_text().splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        if "=" in stripped:
            k, v = stripped.split("=", 1)
            data[k.strip()] = v.strip()
    return data


def _write_env(data: dict[str, str]) -> None:
    """Write key=value pairs to .env, preserving any existing non-wizard keys."""
    existing: dict[str, str] = {}
    comments: list[str] = []

    # Read existing file to preserve non-wizard config and comments
    if ENV_FILE.exists():
        for line in ENV_FILE.read_text().splitlines():
            stripped = line.strip()
            if not stripped or stripped.startswith("#"):
                comments.append(line)
                continue
            if "=" in stripped:
                k, v = stripped.split("=", 1)
                existing[k.strip()] = v.strip()

    # Merge: wizard data overrides, rest preserved
    existing.update(data)

    lines: list[str] = []
    # Write header
    lines.append("# TrustLens AI Configuration")
    lines.append("# Auto-generated by setup wizard — cleared on exit")
    lines.append("")

    for k, v in existing.items():
        lines.append(f"{k}={v}")

    lines.append("")
    ENV_FILE.write_text("\n".join(lines) + "\n")


def _get_saved_config() -> dict[str, str] | None:
    """Check if there's a saved LLM config in .env. Returns provider info or None."""
    env = _read_env()
    provider = env.get("TRUSTLENS_AI_PROVIDER", "").strip()
    if not provider:
        return None

    # Build a summary of what's saved
    info: dict[str, str] = {"provider": provider}
    key_map = {
        "openai": ("TRUSTLENS_OPENAI_MODEL", "TRUSTLENS_OPENAI_API_KEY"),
        "anthropic": ("TRUSTLENS_ANTHROPIC_MODEL", "TRUSTLENS_ANTHROPIC_API_KEY"),
        "grok": ("TRUSTLENS_GROK_MODEL", "TRUSTLENS_GROK_API_KEY"),
        "gemini": ("TRUSTLENS_GEMINI_MODEL", "TRUSTLENS_GEMINI_API_KEY"),
    }
    model_key, api_key = key_map.get(provider, ("", ""))
    if model_key:
        info["model"] = env.get(model_key, "default")
    if api_key:
        raw = env.get(api_key, "")
        info["has_key"] = "yes" if raw else "no"
    return info


def _clear_wizard_config() -> None:
    """Remove wizard-managed keys from .env."""
    if not ENV_FILE.exists():
        return

    existing = _read_env()
    cleaned = {k: v for k, v in existing.items() if k not in WIZARD_KEYS}

    if not cleaned:
        ENV_FILE.unlink(missing_ok=True)
        print(f"\n{YELLOW}🧹 Cleared LLM config — fresh selection next time.{RESET}")
        return

    lines = ["# TrustLens AI Configuration", ""]
    for k, v in cleaned.items():
        lines.append(f"{k}={v}")
    lines.append("")
    ENV_FILE.write_text("\n".join(lines) + "\n")
    print(f"\n{YELLOW}🧹 Cleared LLM config — fresh selection next time.{RESET}")


